def get_mono_mask(T1, T2):
    device = T1.device
    mask = torch.ones((T1, T2))
    mask[0, 0] = 0
    mask[T1-1, T2-1] = 0
    pre = [(0, 0)]
    # 上边界
    for i in range(T2):
        pre_ = []
        for v1, v2 in pre:
            if v1 + 1 < T1:
                pre_.append((v1+1, v2+1))
                mask[v1+1, v2+1] = 0
            if T2 - (v2+1) >= T1 - v1:
                pre_.append((v1, v2+1))
                mask[v1, v2+1] = 0
        pre = set(pre_)
    return mask.bool().to(device)

def format_Wnt(T1, T2, g=0.2):
    device = T1.device
    n_items = torch.arange(0, T1, device=device)/T1
    t_items = torch.arange(0, T2, device=device)/T2
    w = 1 - torch.exp(-(n_items.unsqueeze(1) - t_items.unsqueeze(0))**2/(2*g**2))
    # 中间的值weighth很小，让loss不会注意到他们，非中间的值weight较大
    return w


def get_guided_mono_mask(T1, T2, max_len=80):
    mono_mask = get_mono_mask(T1, T2)
    w = 1 - format_Wnt(T1, T2)
    w_mono = w.masked_fill(mono_mask, -1)
    # 根据最大值逐行进行mask 最大值一定在最中间
    sort_inds = w_mono.argsort(dim=1, descending=True)
    for i in range(T1):
        for v, j in enumerate(sort_inds[i, :]):
            if v < max_len and w_mono[i, j] > 0:
                w_mono[i, j] = 0
            else:
                w_mono[i, j] = 1
    return w_mono.bool()

